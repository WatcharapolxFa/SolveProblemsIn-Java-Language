class Operatorimportance {
    public static void main(String[] args) {
        float a, y, x, c, v;
        a = 5 + 8 * 9;
        y = 10 - 4 + 2;
        x = 10 - (2 + 1);
        c = 5 * 2 - 40 / 5;
        v = 7 + 8 / 2 + 25;

        System.out.println(a);
        System.out.println(y);
        System.out.println(x);
        System.out.println(c);
        System.out.println(v);
    }
}