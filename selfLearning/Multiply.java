public class Multiply {
    public static void main(String[] args) {

        for (int m = 2; m <= 12; m++) {
            System.out.println("แม่ที่  = " + m);
            for (int x = 1; x <= 12; x++) {
                System.out.println(m + " X " + x + " = " + (m * x));

            }
        }

    }
}
